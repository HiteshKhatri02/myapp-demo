package com.myapp.demo.services;

import android.text.TextUtils;
import android.util.Log;

import com.google.firebase.iid.FirebaseInstanceId;
import com.google.firebase.iid.FirebaseInstanceIdService;
import com.myapp.demo.utils.SavedPreference;

/**
 * A class which extend FirebaseInstanceIdService provided the token for device to specify
 * device has unique id for notifications which we will need to send on the server to register our device.
 */
public class MyFirebaseInstanceIDService extends FirebaseInstanceIdService {

    private static final String TAG = "MyInstanceIDLS";

    /**
     * Called if InstanceID token is updated. This may occur if the security of
     * the previous token had been compromised. This call is initiated by the
     * InstanceID provider.
     */
    @Override
    public void onTokenRefresh() {
        // Get updated InstanceID token.
        String refreshedToken = FirebaseInstanceId.getInstance().getToken();
        Log.d(TAG, "Refreshed token: " + refreshedToken);
        storeRegistrationID(refreshedToken);
    }
    /**
     * Persist registration to third-party servers.
     *
     * Modify this method to associate the user's GCM registration token with any server-side account
     * maintained by your application.
     *
     * @param token The new token.
     */
    private void storeRegistrationID(String token) {
        // Add custom implementation, as needed.
        if (!TextUtils.isEmpty(token)) {
            SavedPreference sp = new SavedPreference(this);
            sp.storeDeviceToken(token);
        }

    }

}
